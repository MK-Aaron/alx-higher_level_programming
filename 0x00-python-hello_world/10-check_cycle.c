#include "lists.h"

/**
 * check_cycle - infinite loop list
 * list: pointer to the singly linked list
 * Return: 0 if no cyle otherwise 1
 */

int check_cycle(listint_t *list)
{
	listint_t *slow = list;
	listint_t *fast = list;

	if (!list)
		return (0);

	while (slow && fast && fast->next)
	{
		slow = slow->next;
		fast = fast->next->next;
		if (slow == fast)
			return (1);
	}
	return (0);
}
